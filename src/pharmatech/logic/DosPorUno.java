public class DosPorUno {
    private int idDosPorUno;
    private String idAriculoCatalogo;
    private int idDiaPromocion;
    private int cantidadCompra;
    private int cantidadRegalo;

    public int getIdDosPorUno() {
        return idDosPorUno;
    }

    public void setIdDosPorUno(int idDosPorUno) {
        this.idDosPorUno = idDosPorUno;
    }

    public String getIdAriculoCatalogo() {
        return idAriculoCatalogo;
    }

    public void setIdAriculoCatalogo(String idAriculoCatalogo) {
        this.idAriculoCatalogo = idAriculoCatalogo;
    }

    public int getIdDiaPromocion() {
        return idDiaPromocion;
    }

    public void setIdDiaPromocion(int idDiaPromocion) {
        this.idDiaPromocion = idDiaPromocion;
    }

    public int getCantidadCompra() {
        return cantidadCompra;
    }

    public void setCantidadCompra(int cantidadCompra) {
        this.cantidadCompra = cantidadCompra;
    }

    public int getCantidadRegalo() {
        return cantidadRegalo;
    }

    public void setCantidadRegalo(int cantidadRegalo) {
        this.cantidadRegalo = cantidadRegalo;
    }
    
}
